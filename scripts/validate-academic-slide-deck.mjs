#!/usr/bin/env node
import fs from 'node:fs';

const file = process.argv[2];
if (!file) {
  console.error('Usage: node scripts/validate-academic-slide-deck.mjs path/to/deck.html');
  process.exit(2);
}

const html = fs.readFileSync(file, 'utf8');
const allowed = new Set([
  'A01_TITLE',
  'A02_SECTION',
  'A03_RESEARCH_PUZZLE',
  'A04_LITERATURE_GAP',
  'A05_THEORY_FRAMEWORK',
  'A06_EXPECTATIONS',
  'A07_DATA_DESIGN',
  'A08_MEASUREMENT',
  'A09_METHOD_MODEL',
  'A10_MAIN_RESULT_FIGURE',
  'A11_INTERACTION_OR_HETEROGENEITY',
  'A12_MECHANISM_RESULT',
  'A13_TABLE_SUMMARY',
  'A14_ROBUSTNESS',
  'A15_LIMITATIONS_SCOPE',
  'A16_CONTRIBUTIONS',
  'A17_DISCUSSION_QA',
  'A18_CLOSING',
  'A19_BACKUP_APPENDIX'
]);

const errors = [];
const warnings = [];
const sectionRegex = /<section\b[^>]*class=["'][^"']*\bslide\b[^"']*["'][^>]*>/gi;
const sections = html.match(sectionRegex) || [];

if (sections.length === 0) errors.push('No <section class="slide"> elements found.');

sections.forEach((tag, idx) => {
  const layout = tag.match(/data-layout=["']([^"']+)["']/i)?.[1];
  if (!layout) errors.push(`Slide ${idx + 1} is missing data-layout.`);
  else if (!allowed.has(layout)) errors.push(`Slide ${idx + 1} uses unknown layout ${layout}.`);
});

if (!/width:\s*1920px/.test(html) || !/height:\s*1080px/.test(html)) {
  warnings.push('Could not verify fixed 1920×1080 stage dimensions.');
}
if (/overflow\s*:\s*(auto|scroll)/i.test(html)) {
  errors.push('Slides should not use scrolling overflow. Split dense content instead.');
}

const smallFontMatches = [...html.matchAll(/font-size\s*:\s*(\d+(?:\.\d+)?)px/gi)]
  .map(m => Number(m[1]))
  .filter(size => size > 0 && size < 18);
if (smallFontMatches.length > 0) warnings.push(`Found ${smallFontMatches.length} font-size declarations below 18px. Check legibility.`);

const forbidden = [
  ['placeholder TODO marker', /\bTODO\b/i],
  ['lorem ipsum', /lorem\s+ipsum/i],
  ['raw regression-output language', /Std\.\s*Err\.|\bz\s*=\s*[-+0-9.]|\bt\s*=\s*[-+0-9.]|Pr\(>|p-value\s+without\s+interpretation/i],
  ['internal preview labels', /style option|preview\.md|template slug|generated by ai/i]
];
for (const [label, pattern] of forbidden) {
  if (pattern.test(html)) warnings.push(`Potential placeholder or internal text detected: ${label}.`);
}

const crestCount = (html.match(/class=["'][^"']*\bcrest\b[^"']*["']/gi) || []).length;
if (crestCount > sections.length) warnings.push('Crest appears more often than slide count. Verify it is not being used decoratively.');

if (errors.length) {
  console.error('Academic slide deck validation failed:');
  errors.forEach(e => console.error(`  P0: ${e}`));
  warnings.forEach(w => console.warn(`  Warning: ${w}`));
  process.exit(1);
}

console.log(`Academic slide deck validation passed for ${sections.length} slide(s).`);
warnings.forEach(w => console.warn(`Warning: ${w}`));
